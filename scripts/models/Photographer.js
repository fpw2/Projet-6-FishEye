class Photographer {
    constructor(data) {
        this.name = data.name
        this.city = data.city
        this.country = data.country
        this.tagline = data.tagline
        this.portrait = data.portrait
    }

    get name() {
        return this.name
    }
    get city() {
        return this.city
    }
    get country() {
        return this.country
    }
    get tagline() {
        return this.tagline
    }
    get portrait() {
        return this.portrait
    }

}

